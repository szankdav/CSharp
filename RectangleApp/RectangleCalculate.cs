using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace RectangleApp
{
    public class RectangleCalculate
    {
        public int getRectanglePerimeter(int a, int b)
        {
            return (a + b) * 2;
        }

        public int getRectangleArea(int a, int b)
        {
            return a * b;
        }
	}
}
